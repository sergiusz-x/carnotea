import { writeFile } from 'node:fs/promises';
import { pathToFileURL } from 'node:url';

import { z } from 'zod';

const METHODS = ['get', 'put', 'post', 'delete', 'options', 'head', 'patch', 'trace'];

const openApiDocumentSchema = z.looseObject({
  openapi: z.string(),
  paths: z.record(z.string(), z.unknown()),
  components: z.record(z.string(), z.unknown()).optional(),
});

function quote(value) {
  return JSON.stringify(value);
}

function indent(value, spaces = 2) {
  const prefix = ' '.repeat(spaces);
  return value
    .split('\n')
    .map((line) => (line ? `${prefix}${line}` : line))
    .join('\n');
}

function schemaType(schema) {
  if (!schema || typeof schema !== 'object') return 'unknown';
  if (Array.isArray(schema.type)) {
    return schema.type.map((type) => schemaType({ ...schema, type })).join(' | ');
  }
  if ('$ref' in schema) {
    const segments = schema.$ref.replace('#/', '').split('/');
    return segments
      .map((segment) => `[${quote(segment)}]`)
      .join('')
      .replace(/^/, 'components');
  }
  if ('const' in schema) return quote(schema.const);
  if (Array.isArray(schema.enum)) return schema.enum.map(quote).join(' | ');

  const variants = schema.oneOf ?? schema.anyOf;
  if (Array.isArray(variants)) return variants.map(schemaType).join(' | ');
  if (Array.isArray(schema.allOf)) return schema.allOf.map(schemaType).join(' & ');

  if (schema.type === 'array') return `(${schemaType(schema.items)})[]`;
  if (schema.type === 'object' || schema.properties) {
    const required = new Set(schema.required ?? []);
    const properties = Object.entries(schema.properties ?? {}).map(
      ([name, property]) =>
        `${name}${required.has(name) ? '' : '?'}: ${schemaType(property)};`,
    );
    if (schema.additionalProperties) {
      properties.push(`[key: string]: ${schemaType(schema.additionalProperties)};`);
    }
    return `{\n${indent(properties.join('\n'))}\n}`;
  }
  if (schema.type === 'string') return 'string';
  if (schema.type === 'integer' || schema.type === 'number') return 'number';
  if (schema.type === 'boolean') return 'boolean';
  if (schema.type === 'null') return 'null';
  return 'unknown';
}

function contentType(content) {
  return `{\n${indent(
    Object.entries(content ?? {})
      .map(([mediaType, media]) => `${quote(mediaType)}: ${schemaType(media.schema)};`)
      .join('\n'),
  )}\n}`;
}

function responsesType(responses) {
  const entries = Object.entries(responses ?? {}).map(([status, response]) => {
    const key = /^\d+$/.test(status) ? status : quote(status);
    return `${key}: { content: ${contentType(response.content)} };`;
  });
  return `{\n${indent(entries.join('\n'))}\n}`;
}

function operationType(operation) {
  const fields = [`responses: ${responsesType(operation.responses)};`];
  if (operation.requestBody?.content) {
    fields.unshift(`requestBody: { content: ${contentType(operation.requestBody.content)} };`);
  }
  return `{\n${indent(fields.join('\n'))}\n}`;
}

export function generateSchema(document) {
  const pathEntries = Object.entries(document.paths ?? {}).map(([path, item]) => {
    const operations = METHODS.filter((method) => item[method]).map(
      (method) => `${method}: ${operationType(item[method])};`,
    );
    return `${quote(path)}: {\n${indent(operations.join('\n'))}\n};`;
  });
  const componentEntries = Object.entries(document.components ?? {}).map(([group, values]) => {
    const schemas = Object.entries(values ?? {}).map(
      ([name, schema]) => `${quote(name)}: ${schemaType(schema)};`,
    );
    return `${quote(group)}: {\n${indent(schemas.join('\n'))}\n};`;
  });

  return `/**\n * This file was auto-generated by scripts/generate-api-schema.mjs.\n * Do not make direct changes to the file.\n */\n\nexport interface paths {\n${indent(pathEntries.join('\n'))}\n}\n\nexport interface components {\n${indent(componentEntries.join('\n'))}\n}\n`;
}

async function main() {
  const [source, output] = process.argv.slice(2);
  if (!source || !output) throw new Error('Usage: generate-api-schema <source-url> <output-file>');

  const response = await fetch(source);
  if (!response.ok) throw new Error(`OpenAPI request failed: ${response.status}`);
  const document = openApiDocumentSchema.parse(await response.json());
  await writeFile(output, generateSchema(document));
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await main();
}
